const dialog = document.querySelector('dialog');

function createArticle(element_id, title, image, img_alt, caption, description) {
  const element_node = document.getElementById(element_id);

  // <article>
  const article_node = document.createElement('article');

  // <h2>
  const h2_node = document.createElement('h2');
  const h2_text_node = document.createTextNode(title);
  h2_node.appendChild(h2_text_node);

  // <figure>
  const figure_node = document.createElement('figure');
  
  // <img>
  const img_node = document.createElement('img');
  img_node.setAttribute('src', image);
  img_node.setAttribute('alt', img_alt);
  figure_node.appendChild(img_node);

  // <figcaption>
  const figcaption_node = document.createElement('figcaption');
  const figcaption_text_node = document.createTextNode(caption);
  figcaption_node.appendChild(figcaption_text_node);
  figure_node.appendChild(figcaption_node);

  // <p>
  const p_node = document.createElement('p');
  const p_text_node = document.createTextNode(description);
  p_node.appendChild(p_text_node);

  // Appends to article
  article_node.appendChild(h2_node);
  article_node.appendChild(figure_node);
  article_node.appendChild(p_node);

  // Append to element
  element_node.appendChild(article_node);

  return;
}


function createModal(dialog, article, image) {
  article.addEventListener('click', function() {
    dialog.showModal();
    document.querySelector('dialog > img').setAttribute('src', image);
  })
  return
}


